export const worldCountriesByLocale = {
	ru: worldCountriesRu,
	en: worldCountriesEn,
	tt: worldCountriesTt,
	qt: worldCountriesQt,
	uk: worldCountriesUk,
	de: worldCountriesDe,
	es: worldCountriesEs,
	ky: worldCountriesKy,
	uz: worldCountriesUz,
	kk: worldCountriesKk,
} satisfies Record<AppLocale, string[]>;

type CountryLabels = Record<AppLocale, string>;

export const countryLabelsByRu = Object.fromEntries(
	worldCountriesRu.map((country, index) => [
		country,
		Object.fromEntries(
			Object.entries(worldCountriesByLocale).map(([locale, values]) => [locale, values[index] ?? country]),
		),
	]),
) as Record<string, CountryLabels>;

export function getCountryLabels(country: string): CountryLabels {
	return (
		countryLabelsByRu[country] ?? {
			ru: country,
			en: country,
			tt: country,
			qt: country,
			uk: country,
			de: country,
			es: country,
			ky: country,
			uz: country,
			kk: country,
		}
	);
}

export function getCountryLabel(country: string, locale: AppLocale): string {
	return getCountryLabels(country)[locale] ?? country;
}
